SELECT BICYCLES.BIKE_ID AS "BICYCLE ID NUMBER" , 
BICYCLES.MODEL AS "BICYCLE MODEL" , 
BICYCLES.COLOR AS "BICYCLE COLOR" , 
PURCHASE_RECORDS.CELLPHONE AS "PURCHASER CELL PHONE",
REPAIR_DATA.CELLPHONE AS "REPAIR CUSTOMER CELLPHONE"


FROM (BICYCLES

LEFT JOIN PURCHASE_RECORDS
ON PURCHASE_RECORDS.BIKE_ID =
BICYCLES.BIKE_ID)

LEFT JOIN REPAIR_DATA 
ON REPAIR_DATA.BIKE_ID =
BICYCLES.BIKE_ID